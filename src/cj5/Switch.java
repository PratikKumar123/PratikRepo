package cj5;
//using switch and break
import java.util.Scanner;

public class Switch {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		String s = sc.nextLine();
		s=s.toLowerCase();
		switch(s) {
		
		case "pr": System.out.println("pratik");
					break;
		case "pratik":
			System.out.println("pratik1");
			break;
		
		}
		
		

	}

}
